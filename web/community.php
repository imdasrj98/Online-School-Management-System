<h1>Community Services</h1>
<p>
<font size="5">
LKS places a lot of importance on social responsibility of the students. It inculcates the habit of social giving in the students through different activities. It has the Social Involvement Programme. Donating things on religious festival to the deprived and parentless children in the orphanage is a yearly feature. Collecting school supplies to offer, planting a school garden, holding a recycling habit , planting trees, creating a new trail at a nature centre, collecting used clothes to be donated, making first aid kits for homeless shelters are few of them that the students are expected to be involved in.
